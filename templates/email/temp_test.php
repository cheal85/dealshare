<p>Get started in three easy steps:</p>

<ol>
  <li>Open any Lucidchart document.</li>
  <li>Select the Chat window on the lower-left side of the screen.</li>
  <li>Click "Start a Hangout" and invite participants by email address.</li>
</ol>
<p>If your document was previously shared with colleagues, their names will be automatically added to the invitation list, but you can add or remove anyone you wish before invites are sent.</p>

