<a href="https://www.lucidchart.com/km?rurl=https%3A%2F%2Fwww.lucidchart.com%2Fblog%2F2014%2F04%2F30%2Flucidchart-integrates-with-google-hangouts%2F&amp;id=100013710&amp;property_name=Link&amp;property_value=NEWS1404Hangouts&amp;event=Clicked%20E-mail%20Link&amp;hash=51bd6a98915572f815ef70d1577ec7f5" target="_blank"><center><img src="https://ci3.googleusercontent.com/proxy/AmnJ0zDPsbsS-DSQDH5LVmRebWofTindofROgtNmqoUNZVCWPQAPrmB3FehIkCkjIquKD6TPAvAWkb6fH07xWL6PI2joeY1-rntEj_1L5BY_J8bidTat8ZEV9R6Kig=s0-d-e1-ft#https://cdn-lucidchart-com.s3.amazonaws.com/blog/hangout_screenshot4.png" width="85%" alt="Google Hangout Lucidchart"></center></a>

